    split_path = os.path.join(SOURCE_DATASET, split)

    # Skip if split doesn't exist
    if not os.path.exists(split_path):
        print(f"⚠️ Split '{split}' not found at {split_path} - skipping")
        continue

    # Get all classes in this split
    try:
        classes = sorted(os.listdir(split_path))
    except NotADirectoryError:
        print(f"⚠️ {split_path} is not a directory - skipping")
        continue

    for cls in classes:

        class_path = os.path.join(split_path, cls)

        if not os.path.isdir(class_path):
            continue

        # Get all images
        try:
            images = [
                img for img in os.listdir(class_path)
                if img.lower().endswith(IMAGE_EXTENSIONS)
            ]
        except FileNotFoundError:
            print(f"⚠️ Class path not found: {class_path} - skipping")
            continue

        total_images = len(images)

        # Skip empty directories
        if total_images == 0:
            print(f"{split}/{cls}")
            print(f"⚠️ No images found - skipping")
            print("-"*40)
            continue

        # Calculate sample size (minimum 1)
        sample_size = max(1, int(total_images * PERCENTAGE))

        # Sample images
        sampled_images = random.sample(images, sample_size)

        # Create destination folder
        destination_folder = os.path.join(
            DEST_DATASET,
            split,
            cls
        )

        os.makedirs(destination_folder, exist_ok=True)

        # Copy images with error handling
        success_count = 0
        for image in sampled_images:

            src = os.path.join(class_path, image)
            dst = os.path.join(destination_folder, image)

            try:
                shutil.copy2(src, dst)
                success_count += 1
            except FileNotFoundError:
                print(f"⚠️ File not found: {src} - skipping")
            except Exception as e:
                print(f"⚠️ Error copying {src}: {e} - skipping")

        print(f"{split}/{cls}")
        print(f"Original : {total_images}")
        print(f"Copied   : {success_count} / {sample_size}")
        print("-"*40)


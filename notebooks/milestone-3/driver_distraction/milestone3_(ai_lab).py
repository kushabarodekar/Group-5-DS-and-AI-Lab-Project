from tqdm import tqdm

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

print(device)

train_dir = "/content/drive/MyDrive/Driver_Activity_Dataset_20Percent/train"
val_dir = "/content/drive/MyDrive/Driver_Activity_Dataset_20Percent/val"

transform_train = transforms.Compose([
    transforms.Resize((224,224)),
    transforms.RandomHorizontalFlip(),
    transforms.ToTensor(),
])

transform_val = transforms.Compose([
    transforms.Resize((224,224)),
    transforms.ToTensor(),
])

train_dataset = datasets.ImageFolder(train_dir, transform_train)
val_dataset = datasets.ImageFolder(val_dir, transform_val)

train_loader = DataLoader(train_dataset,
                          batch_size=32,
                          shuffle=True)

val_loader = DataLoader(val_dataset,
                        batch_size=32,
                        shuffle=False)

num_classes = len(train_dataset.classes)

print(train_dataset.classes)

def get_model(name):

    if name=="MobileNetV3":

        model = models.mobilenet_v3_large(weights="DEFAULT")
        in_features = model.classifier[3].in_features
        model.classifier[3] = nn.Linear(in_features,num_classes)

    elif name=="ResNet50":

        model = models.resnet50(weights="DEFAULT")
        in_features = model.fc.in_features
        model.fc = nn.Linear(in_features,num_classes)

    elif name=="EfficientNetB0":

        model = models.efficientnet_b0(weights="DEFAULT")
        in_features = model.classifier[1].in_features
        model.classifier[1] = nn.Linear(in_features,num_classes)

    return model.to(device)

def train_one_model(model):

    criterion = nn.CrossEntropyLoss()

    optimizer = optim.AdamW(model.parameters(),lr=0.001)

    epochs = 10

    model.train()

    for epoch in range(epochs):

        running_loss=0

        for images,labels in tqdm(train_loader):

            images=images.to(device)
            labels=labels.to(device)

            optimizer.zero_grad()

            outputs=model(images)

            loss=criterion(outputs,labels)

            loss.backward()

            optimizer.step()

            running_loss+=loss.item()

        print(f"Epoch {epoch+1} Loss={running_loss/len(train_loader):.4f}")

def evaluate(model):

    model.eval()

    preds=[]
    labels_list=[]

    with torch.no_grad():

        for images,labels in val_loader:

            images=images.to(device)

            outputs=model(images)

            pred=torch.argmax(outputs,1)

            preds.extend(pred.cpu().numpy())

            labels_list.extend(labels.numpy())

    accuracy=accuracy_score(labels_list,preds)

    precision,recall,f1,_=precision_recall_fscore_support(
        labels_list,
        preds,
        average="weighted"
    )

    return accuracy,precision,recall,f1

